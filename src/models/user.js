import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  profilePicture: {
    type: String,
    default: '',
  },
    bio: {
    type: String,
    default: '',
  },
  followers: [{
    type:mongoose.Schema.Types.ObjectId,
    ref:'User',
    default: [],
  }],
  following: [{
    type:mongoose.Schema.Types.ObjectId,
    ref:'User',
    default: [],
  }],
  role: {
    type: String,
    enum: ['user', 'admin'],
    default: 'user',
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const User = mongoose.model('User', userSchema);
export default User;